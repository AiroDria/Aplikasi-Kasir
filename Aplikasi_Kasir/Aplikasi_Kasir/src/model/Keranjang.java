public class Keranjang {
    private String nodetail;
    private String nopenjualan;
    private String noproduk;
    private String jumlah;
    private String subtotal;
    private String nama;
    private String harga;
    private String stok;

    public String getNodetail() {
        return nodetail;
    }

    public void setNodetail(String nodetail) {
        this.nodetail = nodetail;
    }

    public String getNopenjualan() {
        return nopenjualan;
    }

    public void setNopenjualan(String nopenjualan) {
        this.nopenjualan = nopenjualan;
    }

    public String getNoproduk() {
        return noproduk;
    }

    public void setNoproduk(String noproduk) {
        this.noproduk = noproduk;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }

    public String getSubtotal() {
        return subtotal;
    }

    public void setSubtotal(String subtotal) {
        this.subtotal = subtotal;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }

    public String getStok() {
        return stok;
    }

    public void setStok(String stok) {
        this.stok = stok;
    }
}
